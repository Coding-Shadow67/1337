#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	ac;

	ac = argc - 1;
	while (ac > 0)
	{
		i = 0;
		while (argv[ac][i] != '\0')
		{
			write(1, &argv[ac][i], 1);
			i++;
		}
		write(1, "\n", 1);
		ac--;
	}
	return (0);
}
